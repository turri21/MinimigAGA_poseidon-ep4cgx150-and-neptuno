//// BootClearScreen() ////
void BootClearScreen(unsigned short *adr, int size)
{
	int i;
	adr=(unsigned short *)(((int)adr & 0x7fffff) ^ 0x580000);
	for (i=0; i<size; i++) {
		*adr++=0;
	}
}


//// BootUploadLogo() ////
void BootUploadLogo()
{
	RAFile file;
	int x,y;
	int i=0;
	unsigned char *adr;

	if (RAOpen(&file, LOGO_FILE)) {
		RARead(&file, sector_buffer, 512);
		for (y=0; y<LOGO_HEIGHT; y++) {
			adr = (unsigned char *)((SCREEN_BPL1+LOGO_OFFSET+y*(SCREEN_WIDTH/8))^0x580000);
			for (x=0; x<LOGO_WIDTH/16; x++) {
				if (i == 512) {
					RARead(&file, sector_buffer, 512);
					i = 0;
				}
				*adr++=sector_buffer[i++];
				*adr++=sector_buffer[i++];
			}
		}
		for (y=0; y<LOGO_HEIGHT; y++) {
			adr = (unsigned char *)((SCREEN_BPL2+LOGO_OFFSET+y*(SCREEN_WIDTH/8))^0x580000);
			for (x=0; x<LOGO_WIDTH/16; x++) {
				if (i == 512) {
					RARead(&file, sector_buffer, 512);
					i = 0;
				}
				*adr++=sector_buffer[i++];
				*adr++=sector_buffer[i++];
			}
		}
	}
  ClearError(ERROR_FILESYSTEM);
}


//// BootUploadBall() ////
void BootUploadBall()
{
	RAFile file;
	int x;
	int i=0;
	unsigned char *adr;

	if (RAOpen(&file, BALL_FILE)) {
		adr = (unsigned char *)(BALL_ADDRESS ^ 0x580000);
		RARead(&file, adr, BALL_SIZE);
	}
	ClearError(ERROR_FILESYSTEM);
}


//// BootUploadCopper() ////
void BootUploadCopper()
{
	RAFile file;
	int x;
	int i=0;
	unsigned char *adr;

	if (RAOpen(&file, COPPER_FILE)) {
		adr = (unsigned char *)(COPPER_ADDRESS ^ 0x580000);
		RARead(&file, adr, COPPER_SIZE);
	} else {
		unsigned short *p = (unsigned short *)(COPPER_ADDRESS ^ 0x580000);
		*p++=0x00e0; *p++=0x0008;
		*p++=0x00e2; *p++=0x0000;
		*p++=0x00e4; *p++=0x0008;
		*p++=0x00e6; *p++=0x5000;
		*p++=0x0100; *p++=0xa200;
		*p++=0xffff; *p++=0xfffe;
	}
	ClearError(ERROR_FILESYSTEM);
}

#include "bootcustominit.h"

//// BootCustomInit() ////
void BootCustomInit()
{
	unsigned char *upload=(unsigned char *)(0x780000^0x580000);
	unsigned char *src=bootcustominit_bin;
	int i=bootcustominit_bin_len;
	while(--i)
		*upload++=*src++;
}


//// BootInit() ////
void BootInit()
{
//  BootEnableMem();
	// TEMP enable 1MB memory
	EnableOsd();
	SPI(OSD_CMD_MEM);
	SPI(0x5);
	DisableOsd();

	// Put the CPU in reset while we upload init data.
	EnableOsd();
	SPI(OSD_CMD_RST);
	SPI(SPI_CPU_HLT | SPI_RST_CPU);
	DisableOsd();

	BootClearScreen(SCREEN_ADDRESS, SCREEN_MEM_SIZE);
	BootUploadLogo();
	BootUploadBall();
	BootUploadCopper();
	BootCustomInit();

	EnableOsd();
	SPI(OSD_CMD_RST);
	SPI(0); // Allow the CPU to run 
	DisableOsd();
}


